class Solution
{
	public:
		string is_palindrome(int n)
		{
		  string s1 = to_string(n);
		  string s2 = to_string(n);
		  
		 reverse(s2.begin(),s2.end());
		 if(s1 == s2)return "Yes";
		 return "No";
		}
};

