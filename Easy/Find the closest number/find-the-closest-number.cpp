class Solution{
    public:
    int findClosest( int n, int k,int arr[]) 
    { int ans = abs(k-arr[0]);
        int number = arr[0];
        for(int i=1; i<n; i++){
            if(abs(k-arr[i]) <= ans){
                ans = abs(k-arr[i]);
                number = arr[i];
            }
        }
        return number;
    } 
};

